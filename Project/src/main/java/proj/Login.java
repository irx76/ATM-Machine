    private void ButtonLogin_loginpanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonLogin_loginpanelActionPerformed
        // TODO add your handling code here:
        if(TextField_accno_loginpanel.getText().isEmpty() || PasswordFieldPin_loginpanel.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Fields Can't be empty", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int acnm=Integer.parseInt(TextField_accno_loginpanel.getText());
        int pwfd=Integer.parseInt(PasswordFieldPin_loginpanel.getText());
        
        try{
            String url="jdbc:derby://localhost:1527/BankABC";
            Connection con=DriverManager.getConnection(url, "IROSH", "abc@123"); 
            Statement stmnt= con.createStatement();
            String qry="SELECT * FROM IROSH.ACCOUNT";
            ResultSet RS=stmnt.executeQuery(qry); //pointer start from -1.returns false when there are no more rows
            boolean ACwaradi=false,PWwaradi=false;
            while(RS.next()==true){
                int oozanum=RS.getInt("ACC_ID");
                int puz=RS.getInt("PIN");
                String nama=RS.getString("ACC_NAME");
                
                if(acnm==oozanum){
                    ACwaradi=false;
                   
                    if(pwfd==puz){
                        JOptionPane.showMessageDialog(rootPane, "Welcome "+nama, "Success", JOptionPane.NO_OPTION);
                        new MainMenu(oozanum,ll).setVisible(true);
                        this.setVisible(false);
                        PWwaradi=false;
                        
                        File f=new File("C:\\Users\\irosh\\OneDrive\\Desktop\\JAVA\\ProjectBackup\\Project\\Login_log.txt");
                        FileWriter fw=new FileWriter(f,true);
                        fw.write("\n"+oozanum+"\t\t"+nama+"\t\t"+showDate());
                        
                        
                        
                        fw.flush();
                        fw.close();
                        
                        break;
                    }
                    else{
                        PWwaradi=true;
                        break;
                        
                    }
                }
                else{
                   ACwaradi= true;
                   
                }
            }
            if(PWwaradi==true){
                JOptionPane.showMessageDialog(rootPane, "Wrong Password.Try again", "Error", JOptionPane.ERROR_MESSAGE);
                ButtonREset_loginpanelActionPerformed(evt);
            }
            if(ACwaradi==true){
                JOptionPane.showMessageDialog(rootPane, "Wrong Account number.Try again", "Error", JOptionPane.ERROR_MESSAGE);
                ButtonREset_loginpanelActionPerformed(evt);
            }
        }
        catch(Exception e){
                    System.out.println(e.getMessage());
        }
        }
    }//GEN-LAST:event_ButtonLogin_loginpanelActionPerformed

