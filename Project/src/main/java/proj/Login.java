    private void TextField_accno_loginpanelKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TextField_accno_loginpanelKeyPressed
        // TODO add your handling code here:
        String accno=TextField_accno_loginpanel.getText();
        int length=accno.length();
        if(length<6){
            TextField_accno_loginpanel.setEditable(true);//If more than 10 ints, cant edit
        }
        else{
            TextField_accno_loginpanel.setEditable(false);
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE |evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                TextField_accno_loginpanel.setEditable(true);
            }
        }
    }//GEN-LAST:event_TextField_accno_loginpanelKeyPressed

