    private void TextField_ID_adminpanelKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TextField_ID_adminpanelKeyPressed
        // TODO add your handling code here:
        String accno=TextField_ID_adminpanel.getText();
        int length=accno.length();
        if(length<5){
            TextField_ID_adminpanel.setEditable(true);//If more than 10 ints, cant edit
        }
        else{
            TextField_ID_adminpanel.setEditable(false);
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE |evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
                TextField_ID_adminpanel.setEditable(true);
            }
        }
    }//GEN-LAST:event_TextField_ID_adminpanelKeyPressed

