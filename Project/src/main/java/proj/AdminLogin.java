    private void ButtonLogin_loginpanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonLogin_loginpanelActionPerformed
        // TODO add your handling code here:
        if(TextField_ID_adminpanel.getText().isEmpty() || PasswordFieldPin_ADMINpanel.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Fields Can't be empty", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            int acid=Integer.parseInt(TextField_ID_adminpanel.getText());
            int pwfd=Integer.parseInt(PasswordFieldPin_ADMINpanel.getText());

            try{
                String url="jdbc:derby://localhost:1527/BankABC";
                Connection con=DriverManager.getConnection(url, "IROSH", "abc@123");
                Statement stmnt= con.createStatement();
                String qry="SELECT * FROM IROSH.ADMIN";
                ResultSet RS=stmnt.executeQuery(qry); //pointer start from -1.returns false when there are no more rows
                boolean ACwaradi=false,PWwaradi=false;
                while(RS.next()==true){
                    int oozanum=RS.getInt("ADMIN_ID");
                    int puz=RS.getInt("ADMIN_PW");
                    String nama=RS.getString("ADMIN_NAME");

                    if(acid==oozanum){
                        ACwaradi=false;

                        if(pwfd==puz){
                            JOptionPane.showMessageDialog(rootPane, "Welcome "+nama, "Success", JOptionPane.NO_OPTION);
                            new ADMIN(oozanum,ll).setVisible(true);
                            this.setVisible(false);
                            PWwaradi=false;
                            break;
                        }
                        else{
                            PWwaradi=true;
                            break;

                        }
                    }
                    else{
                        ACwaradi= true;

                    }
                }
                if(PWwaradi==true){
                    JOptionPane.showMessageDialog(rootPane, "Wrong Password.Try again", "Error", JOptionPane.ERROR_MESSAGE);
                    ButtonREset_loginpanelActionPerformed(evt);
                }
                if(ACwaradi==true){
                    JOptionPane.showMessageDialog(rootPane, "Wrong Account ID.Try again", "Error", JOptionPane.ERROR_MESSAGE);
                    ButtonREset_loginpanelActionPerformed(evt);
                }
            }
            catch(Exception e){
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_ButtonLogin_loginpanelActionPerformed

